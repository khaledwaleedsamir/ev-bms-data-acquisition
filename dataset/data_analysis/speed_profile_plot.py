dt = 0.1        # 0.1 s update interval
hold_time = 5   # seconds per value

ramped_signal = []
current_speed = 0

for target_speed in speed_vector:
    time_elapsed = 0
    while time_elapsed < hold_time:
        # Ramp towards target
        if current_speed < target_speed:
            current_speed = min(current_speed + step, target_speed)
        elif current_speed > target_speed:
            current_speed = max(current_speed - step, target_speed)
        ramped_signal.append(current_speed)
        time_elapsed += dt

